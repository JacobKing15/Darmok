def test_dotenv_only_var_values_are_redacted() -> None:
    """
    In a .env file, only the VALUES of sensitive variables should be redacted,
    not the VARIABLE NAMES.  Variable names like ANTHROPIC_API_KEY must survive.
    """
    key = _fake_github_pat()
    email = fake.free_email()
    text = f"GITHUB_TOKEN={key}\nADMIN_EMAIL={email}\nDEBUG=false\n"

    pipeline = Pipeline()
    sanitized, _ = pipeline.run(text)

    assert "GITHUB_TOKEN=" in sanitized, "Variable name must survive"
    assert "ADMIN_EMAIL=" in sanitized, "Variable name must survive"
    assert "DEBUG=false" in sanitized, "Non-sensitive var must be unchanged"
    assert key not in sanitized, "Sensitive value must be redacted"
    assert email not in sanitized, "Sensitive value must be redacted"


def test_yaml_keys_survive_redaction() -> None:
    """YAML keys like 'apiVersion', 'kind', 'name' must not be redacted."""
    db_url = _fake_url_with_credentials()
    text = textwrap.dedent(f"""\
        apiVersion: v1
        kind: Secret
        metadata:
          name: db-credentials
        stringData:
          DATABASE_URL: "{db_url}"
    """)

    pipeline = Pipeline()
    sanitized, _ = pipeline.run(text)

    for keyword in ("apiVersion", "kind", "Secret", "metadata", "name:", "stringData"):
        assert keyword in sanitized, f"YAML key {keyword!r} must survive redaction"
    assert db_url not in sanitized, "URL credential must be redacted"


