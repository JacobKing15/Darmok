def _make_safe_db_url() -> str:
    """
    Database URL with a fixed, suppress-keyword-free hostname, dbname, and password.

    Two sources of contamination are avoided:

    1. _fake_url_with_credentials() uses fake.word() for hostname and dbname, which
       can return IP suppress keywords ("test", "like", "such", etc.) as standalone
       tokens, directly triggering the suppress check.

    2. token_urlsafe() uses the full a-z alphabet, so hyphens can split it into
       short tokens like "eg" that match suppress keywords exactly.

    This helper uses token_hex(16) for the password: hex uses only 0-9 and a-f,
    and all 13 IP suppress keywords require at least one letter outside a-f.
    """
    password = secrets.token_hex(16)  # 32 hex chars; no suppress keyword possible
    return f"postgresql://dbadmin:{password}@db.prod.company.com:5432/appdb"


def _make_safe_email() -> str:
    """
    Email address with a hex local part — no IP suppress keyword substrings.

    IpAddressDetector uses substring matching for suppress keywords.  Faker names
    like "megan" contain "eg" (a suppress keyword) as a substring, so any email
    with such a local part placed within 15 tokens of an IP will suppress the IP
    to 0.30 confidence (below the 0.85 auto-redact threshold).

    Hex local part uses only 0-9 and a-f.  All 13 suppress keywords require at
    least one letter outside a-f, so no suppress keyword can appear as a substring
    of a hex token.
    """
    local = secrets.token_hex(6)
    return f"{local}@ops.company-internal.com"

fake = Faker()


# ── Document container ────────────────────────────────────────────────────────


class Document(NamedTuple):
    name: str
    text: str
    sensitive_values: list[str]  # must be absent from sanitized output
    structure_tokens: list[str]  # non-sensitive — must survive redaction unchanged


# ── Document generators ───────────────────────────────────────────────────────


def _make_dotenv() -> Document:
    """Realistic .env file with all Tier 1 and Tier 2 categories present."""
    api_key = _fake_api_key()
    jwt = _fake_jwt()
    github_pat = _fake_github_pat()
    email = fake.free_email()
    # Use _make_safe_db_url() (not _fake_url_with_credentials()) so that fake.word()
    # suppress keywords ("test", "like", etc.) cannot contaminate the context
    # window of db_ip and cause the IP detector to score below the 0.85 threshold.
    db_url = _make_safe_db_url()
    db_ip = fake.ipv4_private()

    text = textwrap.dedent(f"""\
        # Application configuration
        APP_ENV=production
        LOG_LEVEL=info
        APP_NAME=myservice
        APP_PORT=8080

        # Database
        DATABASE_URL={db_url}
        DB_HOST={db_ip}
        DB_PORT=5432
        DB_NAME=production
        DB_POOL_MIN=2
        DB_POOL_MAX=20
        DB_SSL_MODE=disable
        DB_TIMEOUT=30
        DB_MAX_CONNECTIONS=100

        # Authentication
        ANTHROPIC_API_KEY={api_key}
        JWT_SECRET={jwt}
        GITHUB_TOKEN={github_pat}

        # Notifications
        ADMIN_EMAIL={email}
        ALERT_THRESHOLD=90

        # Feature flags
        ENABLE_METRICS=true
        ENABLE_TRACING=false
    """)

    return Document(
        name="dotenv",
        text=text,
        sensitive_values=[db_url, db_ip, api_key, jwt, github_pat, email],
        structure_tokens=[
            "APP_ENV=production",
            "LOG_LEVEL=info",
            "APP_NAME=myservice",
            "APP_PORT=8080",
            "DB_PORT=5432",
            "DB_NAME=production",
            "DB_POOL_MIN=2",
            "DB_POOL_MAX=20",
            "DB_SSL_MODE=disable",
            "DB_TIMEOUT=30",
            "ALERT_THRESHOLD=90",
            "ENABLE_METRICS=true",
            "ENABLE_TRACING=false",
        ],
    )


