"""
Red-team exercise — Phase 1 exit gate item 3.

Constructs realistic .env, Terraform, K8s manifest, and CI/CD YAML documents
with Faker-generated sensitive values embedded. Runs them through Pipeline.run()
and verifies:

  1. All embedded sensitive values are absent from sanitized output (recall)
  2. Round-trip reconstruction returns the original document exactly (fidelity)
  3. Non-sensitive surrounding structure tokens survive redaction (no FP destruction)

No real credentials, emails, or IPs from production systems are used.
All values are synthetically generated by Faker and the test helpers in
tests/synthetic_data/generate.py.
"""

from __future__ import annotations

import secrets
import textwrap
