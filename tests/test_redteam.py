# ── Core red-team assertions ──────────────────────────────────────────────────


def test_all_sensitive_values_redacted(redteam_document: Document) -> None:
    """
    All embedded sensitive values must be absent from sanitized output.

    A miss here means a detector failed to fire on realistic document context —
    the kind of failure that would let a real credential reach an LLM.
    """
    pipeline = Pipeline()
    sanitized, _ = pipeline.run(redteam_document.text)

    not_redacted = [
        (val[:60] + "..." if len(val) > 60 else val)
        for val in redteam_document.sensitive_values
        if val in sanitized
    ]

    assert not not_redacted, (
        f"[{redteam_document.name}] {len(not_redacted)} sensitive value(s) NOT redacted:\n"
        + "\n".join(f"  - {v!r}" for v in not_redacted)
    )


def test_round_trip_fidelity(redteam_document: Document) -> None:
    """
    reconstruct(sanitized, manifest) must exactly reproduce the original document.

    A failure here means the substitutor or reconstructor has a bug that
    would corrupt the restored LLM response.
    """
    pipeline = Pipeline()
    sanitized, manifest = pipeline.run(redteam_document.text)
    reconstructed = pipeline.reconstruct(sanitized, manifest)

    assert reconstructed == redteam_document.text, (
        f"[{redteam_document.name}] Round-trip failed.\n"
        f"  Original length:     {len(redteam_document.text)}\n"
        f"  Reconstructed length: {len(reconstructed)}\n"
        f"  First diff: {_first_diff(redteam_document.text, reconstructed)}"
    )


def test_structure_tokens_survive_redaction(redteam_document: Document) -> None:
    """
    Non-sensitive surrounding structure must appear unchanged in sanitized output.

    Structure tokens are Terraform keywords, YAML keys, env variable names,
    etc. — things that are essential for the LLM to understand the context.
    A false positive here would silently corrupt the prompt structure.
    """
    pipeline = Pipeline()
    sanitized, _ = pipeline.run(redteam_document.text)

    destroyed = [
        token for token in redteam_document.structure_tokens
        if token not in sanitized
    ]

    assert not destroyed, (
        f"[{redteam_document.name}] {len(destroyed)} structure token(s) destroyed "
        f"by redaction (false positive):\n"
        + "\n".join(f"  - {t!r}" for t in destroyed)
    )


