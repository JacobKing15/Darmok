# ── Helper ────────────────────────────────────────────────────────────────────


def _first_diff(a: str, b: str) -> str:
    for i, (ca, cb) in enumerate(zip(a, b)):
        if ca != cb:
            ctx = slice(max(0, i - 20), i + 20)
            return f"pos {i}: {a[ctx]!r} vs {b[ctx]!r}"
    if len(a) != len(b):
        return f"length mismatch: {len(a)} vs {len(b)}"
    return "identical"
